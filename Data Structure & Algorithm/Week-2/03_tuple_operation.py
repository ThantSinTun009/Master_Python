# Tuple operations

myTuple = (1, 4, 3, 2, 2, 2, 5)
myTuple1 = (1, 2, 6, 9, 8, 7)

print(myTuple + myTuple1)

print(myTuple * 4)

print(4 in myTuple1)


print(myTuple.count(2))

print(myTuple.index(4))

print(len(myTuple))



print(max(myTuple))


print(tuple([1, 2, 3, 4]))

